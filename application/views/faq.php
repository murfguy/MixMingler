				</h4>
			</div>
			<div id="user_q0" class="panel-collapse collapse" style="height: 0px;">
				<div class="panel-body">
					<p>We routinely and automatically scan Mixer for online streamers, but in order to minimize calls to Mixer's API we only scan streamers who have 25 or more followers. If you are under 25 follows you can't be scanned and thus do not show up in a list of timelines or recent streams. If your profile is visited, it will still sync basic information and take note of any interesting milestones you've reached.</p>
