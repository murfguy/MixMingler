					<a href="#" class="ing">What are my 'Core Communities'?</a>
				</h4>
			</div>
			<div id="comm_q2" class="panel-collapse collapse" style="height: 0px;">
				<div class="panel-body">
					<p><i>Core Communities</i> are the communities that best represent the type of streams you create. You are allowed to select up to four of your joined communities to be your core communities. Marking a core community will give you more weight in being listed in a community, and when we suggest streamers to viewers.</p>
