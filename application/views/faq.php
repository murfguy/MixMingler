	<h2>Community Questions</h2>
	<div class="panel-group" id="communityAccordion">
		<div class="panel panel-default ">
			<div class="panel-heading accordion-toggle question-toggle collapsed" data-toggle="collapse" data-parent="#communityAccordion" data-target="#comm_q0">
				<h4 class="panel-title">
					<a href="#" class="ing">What is the difference between Following and Joining a Community?</a>
				</h4>
			</div>
			<div id="comm_q0" class="panel-collapse collapse" style="height: 0px;">
				<div class="panel-body">
					<h5><span class="label label-primary">Answer</span></h5>
					<p><b><i>Joining</i></b> a community means that you become a counted member of that community and you essentially telling others that you provide the sort of content under that community's banner. Joining will help you connect with streamers who produce similar content to you.</p>
					<p><b><i>Following</i></b> simply means that you are interested in watching the content provided in those communities. Following helps us determine which other streamers you may want to try watching.</p>
					<p>You can do both actions independently of each other. While it may seem odd to "join" a community but not "follow" it, we felt that it there could be a chance that what you watch and what you make could not always inherently line up.</p>
				</div>
			</div>
		</div>
		<div class="panel panel-default ">
			<div class="panel-heading accordion-toggle collapsed question-toggle" data-toggle="collapse" data-parent="#communityAccordion" data-target="#comm_q1">
